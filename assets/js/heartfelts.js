let activeIndex = 0;
const track = document.getElementById('carousel-track');
const OrgLogo = "assets/icons/512x512.png";

function renderSlides() {
  const slidesHTML = heartfeltCarousel.map((item) => {
    const paragraphs = item.llhText.map(p => 
      `<p class="py-1 px-3 m-0 text-left text-gray-700 indent-8 leading-relaxed mb-2">${p}</p>`
    ).join('');

    const song = item.songRecommendation 
      ? `<p class="py-1 px-3 mt-4 m-0 text-gray-600 italic flex items-center"><b>Song recommendation :</b> ${item.songRecommendation}</p>` 
      : '';

    return `
      <div class="flex-none w-full h-full p-6 md:p-10 flex flex-col box-border">
        
        <div class="flex justify-end items-center mb-4 shrink-0">
          <div class="text-right mr-3">
            <p class="font-bold text-gray-800">${item.anonName}</p>
          </div>
          <img class="p-1 rounded-full w-[60px] h-[60px] object-cover border shadow-sm" src="${item.anonImage}" alt="avatar"/>
        </div>

        <div class="flex-grow overflow-y-auto pr-2">
          <div class="mb-4">
            <p class="pt-3 py-1 px-3 text-left text-gray-700 indent-8 rounded-lg w-full bg-black/5 p-4">
              ${item.anonText}
            </p>
          </div>
          
          <hr class="my-6 border-t-2 border-purple-100" />

          <div class="llh-response">
            ${paragraphs}
            ${song}
          </div>
        </div>

        <div class="flex justify-start items-center mt-6 pt-4 border-t border-gray-100 shrink-0">
          <img class="p-1 rounded-full w-[65px] h-[65px] object-cover border shadow-sm" src="${OrgLogo}" alt="LLH logo" />
          <div class="text-left ml-3">
            <p class="font-bold text-gray-800">${item.llhName}</p>
            <p class="text-xs text-gray-400">Your BFF</p>
          </div>
        </div>
      </div>
    `;
  }).join('');

  track.innerHTML = slidesHTML;
}

// Logic to shift slides
function updateSlidePosition() {
  if (track) {
    track.style.transform = `translateX(-${activeIndex * 100}%)`;
  }
}

// Event Listeners for Buttons
document.getElementById('nextBtn')?.addEventListener('click', () => {
  activeIndex = (activeIndex + 1) % heartfeltCarousel.length;
  updateSlidePosition();
});

document.getElementById('prevBtn')?.addEventListener('click', () => {
  activeIndex = (activeIndex - 1 + heartfeltCarousel.length) % heartfeltCarousel.length;
  updateSlidePosition();
});

renderSlides();